public class Day30 {
    public static void main(String[] args) {
        int a = 10;
        int b = 10;

        System.out.println("Nilai a : "+a);
        System.out.println("Nilai b : "+b);

        boolean lebihBesar = (a >= b);
        System.out.println("Apa a lebih besar atau sama dengan a? : "+lebihBesar);

        boolean lebihKecil = (a <= b);
        System.out.println("Apa a lebih kecil atau sama dengan b? : "+lebihKecil);
    }
}
