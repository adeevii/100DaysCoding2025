package Ang_Ang;

public class AngAngAng {
    public static void main(String[] args) {
        final short tahunSekarang = 2024;
        short tahunKemarin = 2023;
        int tahunDepan = 2025;

        System.out.printf("Saat ini tahun %d\n", tahunSekarang);
        System.out.println("Jadi tahun kemarin adalah tahun "+ tahunKemarin);
        System.out.println("Tahun depan berarti "+ tahunDepan);
        System.out.print(tahunSekarang+ ","+ tahunKemarin+ "," +tahunDepan+ "\n");
    }
}
