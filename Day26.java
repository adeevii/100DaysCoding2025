public class Day26 {
    public static void main(String[] args) {
        int a = 10;
        System.out.println("Nilai awal : " +a);

        a += 5;
        System.out.println("After += : " +a);

        a -= 4;
        System.out.println("After -= : " +a);

        a *= 3;
        System.out.println("After *= : " +a);

        a /= 3;
        System.out.println("After /= : " +a);

        a %= 2;
        System.out.println("After %= : " +a);
    }
}
